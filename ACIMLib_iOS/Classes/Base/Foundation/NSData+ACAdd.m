- (NSString *)ac_imageType {
    
    uint8_t c;
    
    [self getBytes:&c length:1];
    
    switch (c) {
            
        case 0xFF:
            
            return @"jpg";
            
        case 0x89:
            
            return @"png";
            
        case 0x47:
            
            return @"gif";
            
        case 0x49:
            
        case 0x4D:
            
            return @"tiff";
    }
    
    return nil;
}

